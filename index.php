    <script>
        document.addEventListener("DOMContentLoaded", function() {

            let currentUserId = localStorage.getItem("chat_user_id");
            if (!currentUserId) {
                currentUserId = "user_" + Math.floor(Math.random() * 100000);
                localStorage.setItem("chat_user_id", currentUserId);
            }

            const chatBtn = document.getElementById("chatBtn");
            const chatModal = document.getElementById("chatModal");
            const closeChat = document.getElementById("closeChat");
            const flotingwp = document.getElementById("flotingwp");
            const messageInput = document.getElementById("messagebox");
            const chatMessages = document.getElementById("chatMessages");
            const sendBtn = document.getElementById("sendBtn");

            let socket;

            function openChat() {
                chatModal.removeAttribute("hidden");
                chatModal.classList.add("is-open");
                chatBtn.setAttribute("aria-expanded", "true");
                if (flotingwp) flotingwp.setAttribute("hidden", "");
                setTimeout(() => messageInput?.focus(), 300);
            }

            function closeModal() {
                chatModal.setAttribute("hidden", "");
                chatModal.classList.remove("is-open");
                chatBtn.setAttribute("aria-expanded", "false");
                if (flotingwp) flotingwp.removeAttribute("hidden");
                chatBtn.focus();
            }

            if (chatBtn) chatBtn.addEventListener("click", openChat);
            if (flotingwp) flotingwp.addEventListener("click", openChat);
            if (closeChat) closeChat.addEventListener("click", closeModal);

            chatModal.addEventListener("click", (e) => {
                if (e.target === chatModal) closeModal();
            });

            document.addEventListener("keydown", (e) => {
                if (e.key === "Escape" && chatModal.classList.contains("is-open")) {
                    closeModal();
                }
            });

            if (messageInput) {
                messageInput.addEventListener("keypress", (e) => {
                    if (e.key === "Enter") {
                        e.preventDefault();
                        sendMessage();
                    }
                });
            }

            if (sendBtn) sendBtn.addEventListener("click", sendMessage);

            function connectWebSocket() {
                socket = new WebSocket("wss://dentalserver.online/ws/");

                socket.onopen = function() {
                    socket.send(JSON.stringify({
                        type: "register_user",
                        user_id: currentUserId
                    }));
                    socket.send(JSON.stringify({
                        type: "load_history",
                        user_id: currentUserId
                    }));
                };

                socket.onmessage = function(event) {
                    let data;
                    try {
                        data = JSON.parse(event.data);
                    } catch (e) {
                        console.error("Invalid message from server:", e);
                        return;
                    }

                    if (data.type === "chat_history") {
                        chatMessages.innerHTML = "";
                        if (data.messages && data.messages.length > 0) {
                            data.messages.forEach(msg => {
                                addMessageToChat(msg.message, msg.sender);
                            });
                        } else {
                            addMessageToChat("👋 Hi! How can we help you today?", "admin");
                        }
                        return;
                    }

                    if (data.type === "chat_to_user") {
                        addMessageToChat(data.message, "admin");
                    }
                };

                socket.onclose = function() {
                    setTimeout(connectWebSocket, 3000);
                };

                socket.onerror = function() {
                    socket.close();
                };
            }

            function sendMessage() {
                const message = messageInput.value.trim();
                if (!message) return;

                if (socket && socket.readyState === WebSocket.OPEN) {
                    socket.send(JSON.stringify({
                        type: "chat_from_user",
                        user_id: currentUserId,
                        message: message
                    }));
                    addMessageToChat(message, "user");
                    messageInput.value = "";
                    messageInput.focus();
                } else {
                    connectWebSocket();
                }
            }

            function addMessageToChat(message, sender) {
                const messageDiv = document.createElement("div");
                messageDiv.setAttribute("role", "status");
                messageDiv.setAttribute(
                    "aria-label",
                    sender === "user" ? "You said: " + message : "Support replied: " + message
                );

                if (sender === "user") {
                    messageDiv.className = "bg-yellow-400 text-black p-3 rounded-xl w-fit ml-auto max-w-[80%] break-words";
                } else {
                    messageDiv.className = "bg-gray-200 text-gray-900 p-3 rounded-xl w-fit max-w-[80%] break-words";
                }

                messageDiv.innerText = message;
                chatMessages.appendChild(messageDiv);
                chatMessages.scrollTop = chatMessages.scrollHeight;
            }

            connectWebSocket();

        });
    </script>

